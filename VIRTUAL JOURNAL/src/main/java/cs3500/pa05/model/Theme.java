package cs3500.pa05.model;

/**
 * The {@code Theme} enum represents different themes that can be used in an application.
 * It provides constants for the light, dark, and blue themes.
 */
public enum Theme {
  /**
   * Represents the light Theme.
   */
  LIGHT,

  /**
   * Represents the dark Theme.
   */
  DARK,

  /**
   * Represents the blue Theme.
   */
  BLUE,

  /**
   * Represents a custom Theme that the user may make.
   */
  CUSTOM

}
