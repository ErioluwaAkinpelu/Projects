package cs3500.pa05.model;

/**
 * The {@code DayOfTheWeek} enum represents the days of the week.
 */
public enum DayOfTheWeek {
  /**
   * The day Monday.
   */
  MONDAY,

  /**
   * The day Tuesday.
   */
  TUESDAY,

  /**
   * The day Wednesday.
   */
  WEDNESDAY,

  /**
   * The day Thursday.
   */
  THURSDAY,

  /**
   * The day Friday.
   */
  FRIDAY,

  /**
   * The day Saturday.
   */
  SATURDAY,

  /**
   * The day Sunday.
   */
  SUNDAY
}
