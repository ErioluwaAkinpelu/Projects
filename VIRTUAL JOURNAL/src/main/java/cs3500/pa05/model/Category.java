package cs3500.pa05.model;

/**
 * The {@code Category} enum represents different categories that can be used in a journal.
 * It provides constants for social, work, and school categories.
 */
public enum Category {
  /**
   * Represents the social category.
   */
  SOCIAL,

  /**
   * Represents the work category.
   */
  WORK,

  /**
   * Represents the school category.
   */
  SCHOOL,

  /**
   * Represents no specific category.
   */
  NONE
}